public class PrivacyMechanismException extends IllegalArgumentException {

	private static final long serialVersionUID = 3855384970413582741L;

	public PrivacyMechanismException() {
		super();
	}

	public PrivacyMechanismException(String message, Throwable cause) {
		super(message, cause);
	}

	public PrivacyMechanismException(String s) {
		super(s);
	}

	public PrivacyMechanismException(Throwable cause) {
		super(cause);
	}

}
